
var primeiro_valor = parseFloat(prompt("Informe o primeiro valor:"));
var segundo_valor = parseFloat(prompt("Informe o segundo valor:"));


if (primeiro_valor > segundo_valor) {

document.write("O primeiro valor é maior que o segundo.");

} else if (primeiro_valor < segundo_valor) {

document.write("O segundo valor é maior.");

} else {

document.write("O primeiro valor é igual ao segundo.");

}